def add(num1, num2):
    """ Adds the second number to the first number.

    Returns the sum of two inputs.
    """
    return num1 + num2

def subtract(num1, num2):
    """ Subtracts the second number from the first number.

    Returns the result of subtracting the second number from the first.
    """
    return num1 - num2

def multiply(num1, num2):
    """ Multiplies the first number by the second number.

    Returns the product of the inputs.
    """
    return num1 * num2

def divide(num1, num2):
    """ Divides the first number by the second number.

    Divides the first input by the second, returning a floating point number.
    """
    return float(num1) / float(num2)

def square(num1):
    """ Squares the first number by the second number.

    Returns the square of the input.
    """
    return num1 ^ 2

def cube(num1):
    """ Cubes the first number by the second number.

    Returns the cube of the input.
    """  
    return num1 ^ 3

def power(num1, num2):
    """ Divides the first number by the second number.

    Returns the result of raising the first input to the power of the second input.
    """
    return num1 ^ num2

def mod(num1, num2):
    """ Gives the remainder when the first number is divided by the second number.

    Returns the remainder when the first input is divided by the second input.
    """
    return num1 % num2
